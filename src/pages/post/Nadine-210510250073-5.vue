  <h2>Kemiskinan: Faktor yang Sering Diabaikan</h2>

  <p>Analisis data dari 514 kabupaten/kota menunjukkan korelasi kuat antara tingkat kemiskinan dan prevalensi pernikahan dini. Di kabupaten dengan kemiskinan di atas 20%, angka pernikahan anak rata-rata 3,4 kali lebih tinggi dibanding kabupaten dengan kemiskinan di bawah 5%.</p>

  <p>Mekanismenya sederhana dan menyedihkan: keluarga miskin seringkali tidak mampu membiayai pendidikan anak perempuan mereka hingga jenjang tinggi. Alternatif yang terlihat hanya dua: bekerja (yang juga tidak selalu tersedia dan aman) atau menikah. Dalam konteks ini, menikahkan anak perempuan bukan karena orang tuanya tidak peduli — tapi karena mereka tidak melihat jalan lain.</p>

