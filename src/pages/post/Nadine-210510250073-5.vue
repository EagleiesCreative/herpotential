  <h2>UU yang Berubah, Realita yang Belum</h2>

  <p>Pada 2019, Indonesia menaikkan batas usia minimum pernikahan dari 16 menjadi 19 tahun untuk perempuan — setelah kampanye panjang oleh koalisi LSM dan tekanan internasional. Ini adalah langkah maju yang signifikan. Tapi ada celah besar yang tersisa: dispensasi kawin.</p>

  <p>Dispensasi kawin adalah mekanisme hukum yang memungkinkan pengadilan agama mengizinkan pernikahan di bawah usia minimum atas dasar "keadaan mendesak." Di atas kertas, ini dimaksudkan untuk kasus-kasus ekstrem. Dalam praktiknya, jumlah permohonan dispensasi kawin justru melonjak pasca-revisi UU Pernikahan.</p>

  <p>Data Mahkamah Agung menunjukkan: pada 2018, ada 13.887 permohonan dispensasi kawin yang diajukan ke pengadilan agama. Pada 2022, angka itu menjadi 63.634 — hampir lima kali lipat dalam empat tahun. Dan 97% permohonannya dikabulkan.</p>

  <div class="pullquote">
