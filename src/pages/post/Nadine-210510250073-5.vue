  <h2>Apa yang Berhasil: Pelajaran dari Daerah yang Menurunkan Angka Pernikahan Anak</h2>

  <p>Tidak semua berita buruk. Ada beberapa kabupaten yang berhasil menurunkan angka pernikahan dini secara signifikan dalam 5 tahun terakhir — dan dari mereka, kita bisa belajar apa yang benar-benar bekerja.</p>

  <p><strong>Kabupaten Lombok Utara (NTB)</strong> berhasil menurunkan angka dispensasi kawin sebesar 41% antara 2019-2023 dengan program tiga jalur: beasiswa untuk perempuan dari keluarga kurang mampu, pelatihan untuk tokoh agama tentang dampak pernikahan dini, dan satuan tugas desa yang memetakan anak-anak yang berisiko.</p>

  <p><strong>Kabupaten Banyumas (Jawa Tengah)</strong> menggunakan pendekatan berbeda: "Kelas Perempuan Berdaya" yang mengajarkan keterampilan wirausaha kepada perempuan muda dan ibu muda, menciptakan alternatif ekonomi yang nyata.</p>

