    <h2>2. Hak Perburuhan — Di Tempat Kerja</h2>

    <div class="law-card">
      <div class="law-num">UU No. 13 Tahun 2003 & Peraturan Pemerintah Turunannya</div>
      <h3>Hak-Hak Perempuan sebagai Pekerja</h3>
      <ul>
        <li><strong>Cuti haid</strong> — hari pertama dan kedua menstruasi, cukup dengan pemberitahuan ke atasan (Pasal 81)</li>
        <li><strong>Cuti melahirkan</strong> — 3 bulan (1,5 bulan sebelum + 1,5 bulan sesudah melahirkan), dapat diperpanjang dengan keterangan dokter (Pasal 82)</li>
        <li><strong>Cuti keguguran</strong> — 1,5 bulan sesuai keterangan dokter (Pasal 82 ayat 2)</li>
        <li><strong>Larangan PHK karena hamil</strong> — pemecatan dengan alasan kehamilan atau persalinan adalah ilegal (Pasal 153)</li>
        <li><strong>Upah tidak dipotong</strong> saat cuti melahirkan dan haid (Pasal 84)</li>
        <li><strong>Fasilitas menyusui</strong> — pengusaha wajib memberikan kesempatan memadai bagi ibu menyusui (PP No. 33 Tahun 2012)</li>
      </ul>
    </div>

    <h2>3. Hak dalam Perkawinan & Keluarga</h2>

    <div class="law-card">
      <div class="law-num">UU No. 1 Tahun 1974 jo. UU No. 16 Tahun 2019 & UU PKDRT</div>
      <h3>Hak Perempuan dalam Institusi Pernikahan</h3>
      <ul>
        <li><strong>Usia minimal menikah</strong> adalah 19 tahun untuk semua gender (UU No. 16/2019 — perubahan atas UU Perkawinan)</li>
        <li><strong>Kekerasan dalam Rumah Tangga (KDRT)</strong> adalah tindak pidana, bukan urusan privat keluarga (UU No. 23 Tahun 2004 / UU PKDRT)</li>
        <li>KDRT mencakup <strong>kekerasan fisik, psikis, seksual, dan penelantaran ekonomi</strong></li>
        <li><strong>Hak atas harta bersama</strong> — setelah cerai, harta yang diperoleh selama pernikahan dibagi sesuai hukum (atau perjanjian pra-nikah)</li>
        <li><strong>Hak asuh anak</strong> — untuk anak di bawah 12 tahun, hak asuh umumnya diberikan kepada ibu kecuali ada alasan khusus</li>
      </ul>
    </div>

    <h2>4. Kekerasan Berbasis Gender Online (KBGO)</h2>

    <div class="law-card">
      <div class="law-num">UU ITE No. 11 Tahun 2008 & UU TPKS</div>
      <h3>Perlindungan dari Kekerasan Digital</h3>
      <ul>
        <li><strong>Penyebaran konten intim tanpa izin (NCII)</strong> — pelaku dapat dijerat UU TPKS Pasal 14 dengan ancaman penjara hingga 12 tahun</li>
        <li><strong>Stalking dan pelecehan digital</strong> — dapat dilaporkan ke Siber Polri (patrolisiber.id)</li>
        <li><strong>Doxxing</strong> (penyebaran data pribadi tanpa izin) — dapat dijerat UU ITE Pasal 32</li>
        <li>SAFEnet menyediakan <strong>pendampingan gratis</strong> untuk korban KBGO di Indonesia</li>
      </ul>
    </div>

    <h2>Cara Mengakses Bantuan Hukum Gratis</h2>

    <div class="highlight-box">
      <h4>📋 Langkah mengakses bantuan hukum gratis:</h4>
      <ul>
        <li><strong>Pos Bantuan Hukum (Posbakum)</strong> — tersedia di setiap Pengadilan Negeri dan Pengadilan Agama, gratis untuk yang tidak mampu</li>
        <li><strong>YLBHI dan jaringannya</strong> — 15 kantor LBH di seluruh Indonesia menyediakan konsultasi dan pendampingan hukum gratis</li>
        <li><strong>LBH APIK</strong> — khusus untuk perempuan dan anak, memiliki kantor di Jakarta, Aceh, Makassar, dan Semarang</li>
        <li><strong>P2TP2A</strong> (Pusat Pelayanan Terpadu Pemberdayaan Perempuan dan Anak) — ada di setiap kabupaten/kota</li>
        <li>Bawa <strong>KTP dan dokumen pendukung</strong> saat meminta bantuan hukum</li>
      </ul>
