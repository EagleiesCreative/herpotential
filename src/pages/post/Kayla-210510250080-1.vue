    <h2>1. UU TPKS — Perlindungan dari Kekerasan Seksual</h2>

    <div class="law-card">
      <div class="law-num">UU No. 12 Tahun 2022</div>
      <h3>Undang-Undang Tindak Pidana Kekerasan Seksual</h3>
      <p>UU TPKS adalah terobosan hukum terbesar untuk perempuan Indonesia dalam dua dekade terakhir. Beberapa poin penting:</p>
      <ul>
        <li><strong>9 jenis kekerasan seksual</strong> diakui secara hukum, termasuk kekerasan seksual berbasis elektronik (KSBE)</li>
        <li><strong>Korban mendapatkan hak restitusi</strong> (ganti rugi dari pelaku) dan pemulihan dari negara</li>
        <li><strong>Pendampingan hukum gratis</strong> bagi korban yang tidak mampu melalui Pos Bantuan Hukum</li>
        <li><strong>Korban bisa melapor</strong> ke kepolisian, P2TP2A, atau langsung ke LPSK (Lembaga Perlindungan Saksi dan Korban)</li>
        <li>Berlaku untuk kekerasan yang terjadi di <strong>ranah privat, publik, maupun digital</strong></li>
      </ul>
