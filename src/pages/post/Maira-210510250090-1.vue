    <h2>Saat Menolak Terasa Berat: Ini Normal</h2>

    <p>Perasaan bersalah setelah menolak adalah respons yang sangat manusiawi, terutama jika kamu tidak terbiasa melakukannya. Psikolog Nirmala Ika dalam podcast Bicara Sehat (2024) menyarankan untuk mengingat satu hal: rasa bersalah itu bukan tanda bahwa kamu salah, tapi tanda bahwa kamu sedang melatih kebiasaan baru.</p>

