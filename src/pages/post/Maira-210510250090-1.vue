    <h2>Mengapa Perempuan Lebih Sulit Berkata Tidak?</h2>

    <p>Menurut Dr. Erlina Zahara, psikolog klinis dari Universitas Indonesia, perempuan sejak kecil sering menerima pesan sosial bahwa kebutuhan mereka harus selalu dinomordua. "Ada tekanan budaya yang membuat perempuan merasa bersalah ketika memprioritaskan diri sendiri. Padahal itu adalah hak dasar setiap manusia," jelasnya dalam artikel Tirto.id (2023).</p>

    <p>Dalam konteks Indonesia, tekanan ini semakin berlapis — ekspektasi keluarga besar, norma "nrimo", hingga stereotype perempuan yang "tidak ramah" jika terlalu tegas. Akibatnya, banyak perempuan memilih diam atau berkata iya sambil memendam rasa tidak nyaman.</p>

    <h2>Tiga Langkah Menolak dengan Sehat</h2>

    <div class="highlight-box">
      <h4>🧠 Framework SET: Singkat, Empati, Tegas</h4>
      <ul>
        <li><strong>Singkat</strong> — Tidak perlu panjang lebar menjelaskan alasanmu. Semakin banyak alasan yang kamu berikan, semakin besar peluang orang lain berdebat.</li>
        <li><strong>Empati</strong> — Akui perasaan atau kebutuhan mereka, tanpa mengiyakan permintaan. "Aku paham kamu butuh ini..."</li>
        <li><strong>Tegas</strong> — Sampaikan batas dengan jelas, tanpa kata-kata yang mengundang negosiasi seperti "mungkin", "kayaknya", atau "ntar deh".</li>
      </ul>
    </div>

    <h2>Script Siap Pakai: Situasi Nyata</h2>

    <h3>Di Tempat Kerja — Ketika Diminta Lembur Terus</h3>
