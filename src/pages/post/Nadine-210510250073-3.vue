  <p>Pola yang terlihat jelas: semakin dekat dengan akar rumput dan komunitas, semakin sedikit perempuan yang duduk di kursi pengambil keputusan. Padahal kebijakan di tingkat kabupaten dan kota — soal posyandu, pendidikan anak, pasar tradisional, keamanan lingkungan — adalah kebijakan yang paling langsung memengaruhi kehidupan sehari-hari perempuan.</p>

  <h2>Lima Hambatan Sistemik yang Perlu Dipahami</h2>

  <h3>1. Biaya Politik yang Sangat Tinggi</h3>
  <p>Menjadi caleg di Indonesia bukan sekadar soal kapasitas dan visi. Ia memerlukan modal finansial yang besar — dari biaya administrasi, kampanye, hingga "setoran" ke partai yang tidak diakui secara resmi tapi nyata adanya. Rata-rata biaya kampanye caleg DPR RI berkisar antara Rp 500 juta hingga Rp 2 miliar. Perempuan, yang secara rata-rata masih memiliki akses lebih terbatas ke modal dan aset, menghadapi hambatan finansial yang lebih besar sejak awal.</p>

  <h3>2. Norma Sosial dan Ekspektasi Ganda</h3>
  <p>Perempuan yang terjun ke politik sering menghadapi pertanyaan yang tidak pernah ditanyakan kepada kandidat laki-laki: "Siapa yang jaga anakmu?" atau "Suamimu mendukung?" Ekspektasi ganda ini — untuk menjadi politisi yang efektif sekaligus ibu dan istri yang "sempurna" — menciptakan beban yang tidak setara.</p>

  <h3>3. Budaya Partai yang Masih Patriarkal</h3>
  <p>Proses pencalonan di banyak partai masih didominasi oleh jaringan laki-laki. Perempuan sering ditempatkan di nomor urut yang tidak strategis (nomor bawah dalam dapil yang kompetitif), atau di daerah pemilihan yang dianggap "tidak mungkin menang."</p>

  <div class="pullquote">
    "Kuota 30% perempuan dalam daftar caleg hanya berfungsi sebagai angka di atas kertas jika perempuan selalu diletakkan di posisi yang tidak memiliki peluang menang."
    <cite>— Nur Priyanti, Peneliti LIPI</cite>
  </div>

  <h3>4. Minimnya Perempuan dalam Struktur Pengurus Partai</h3>
  <p>Dari 18 partai peserta Pemilu 2024, hanya 3 yang memiliki ketua umum perempuan. Di tingkat kepengurusan DPP, rata-rata hanya 18% pengurus adalah perempuan. Ini penting karena struktur partai yang menentukan siapa yang masuk daftar caleg, mendapat dana kampanye, dan didukung oleh mesin partai.</p>

  <h3>5. Kekerasan Politik Berbasis Gender (KPBG)</h3>
  <p>Studi yang dilakukan oleh International IDEA Indonesia pada 2023 menemukan bahwa 67% perempuan anggota DPR/DPRD pernah mengalami setidaknya satu bentuk KPBG — mulai dari komentar seksis, pelecehan verbal dalam rapat, hingga ancaman dan serangan di media sosial. KPBG tidak hanya melukai individu; ia mengirimkan pesan kepada perempuan lain bahwa ruang politik bukan untuk mereka.</p>

  <h2>Yang Bisa Berubah: Agenda Reformasi</h2>

