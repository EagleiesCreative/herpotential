      <li>Tanyakan kepada partai favoritmu: berapa persen pengurus dan caleg mereka perempuan, dan di nomor urut berapa?</li>
      <li>Dukung organisasi seperti Puskapol UI, CETRO, atau Women's Coalition yang mendorong reformasi elektoral</li>
      <li>Jadilah pengawas pemilu yang berperspektif gender di pemilu berikutnya</li>
      <li>Jika kamu berkecimpung di partai: advokasi untuk penempatan caleg perempuan di nomor strategis</li>
    </ul>
  </div>

  <p>Indonesia telah menempuh jalan panjang. Dari nol perempuan di kabinet era Sukarno, hingga wakil presiden perempuan pertama yang dilantik pada 2024. Tapi setiap pencapaian itu tidak datang sendiri — ia datang karena ada perempuan dan laki-laki yang terus mendorong, bernegosiasi, dan menolak menerima status quo sebagai takdir.</p>

  <p>Parlemen yang mewakili rakyat sepenuhnya berarti parlemen yang mewakili perempuan secara setara. Itu bukan angan-angan. Itu agenda politik yang harus kita perjuangkan bersama.</p>

