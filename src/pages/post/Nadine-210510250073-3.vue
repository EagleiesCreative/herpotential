  <p>Pada Pemilu 2024, Indonesia berhasil meningkatkan keterwakilan perempuan di DPR RI menjadi 21,8% — angka tertinggi dalam sejarah negara ini. Sebuah pencapaian yang patut dirayakan. Tapi mari kita juga jujur: 21,8% berarti hampir 4 dari 5 kursi di parlemen kita masih diduduki laki-laki. Dan di tingkat DPRD provinsi maupun kabupaten/kota, angka itu jauh lebih rendah.</p>

