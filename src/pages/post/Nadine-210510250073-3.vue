      <li><strong>Legislasi KPBG yang komprehensif</strong> — aturan yang jelas dan penegakan yang tegas terhadap kekerasan politik berbasis gender</li>
      <li><strong>Program kepemimpinan perempuan</strong> — pendanaan untuk pelatihan caleg perempuan potensial dari daerah 3T</li>
      <li><strong>Transparansi pencalonan partai</strong> — KPU wajib mempublikasikan posisi nomor urut caleg perempuan di setiap dapil</li>
    </ul>
  </div>

  <h2>Belajar dari Rwanda: Bahwa 30% Hanyalah Titik Awal</h2>

  <p>Rwanda hari ini memiliki parlemen dengan 61% anggota perempuan — tertinggi di dunia. Perubahan dramatis ini tidak terjadi karena kulturnya "lebih baik," tapi karena ada kombinasi kebijakan afirmatif yang kuat pasca-genosida 1994, kepemimpinan nasional yang komit, dan gerakan perempuan di akar rumput yang tidak berhenti mendorong.</p>

  <p>Pelajaran dari Rwanda bukan bahwa kita harus meniru konteksnya — tapi bahwa perubahan fundamental dalam representasi politik perempuan adalah mungkin dalam satu generasi, jika ada komitmen kebijakan yang nyata.</p>

  <h2>Apa yang Bisa Kamu Lakukan Sekarang?</h2>

  <p>Kamu tidak perlu jadi caleg untuk mendorong perubahan ini. Beberapa hal yang bisa dimulai hari ini:</p>

  <div class="highlight-box">
    <h4>🗳️ Aksi nyata untuk mendukung representasi perempuan:</h4>
