  <h2>Apa yang Kata Data</h2>

  <p>Data Komisi Pemilihan Umum (KPU) menunjukkan bahwa meski ada aturan kuota 30% perempuan dalam daftar calon legislatif, implementasinya masih jauh dari ideal. Dari 9.917 caleg yang terdaftar dalam Pemilu 2024, 37% adalah perempuan — namun dari yang berhasil terpilih, angkanya anjlok drastis.</p>

