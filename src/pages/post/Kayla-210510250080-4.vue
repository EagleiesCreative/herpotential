    <h2>Menstruasi: Apa yang Normal, Apa yang Tidak</h2>

    <div class="topic-card">
      <div class="topic-tag">Menstruasi</div>
      <h3>Siklus Haid yang Sehat</h3>
      <p>Menstruasi yang sehat berlangsung <strong>21-35 hari sekali</strong>, dengan durasi <strong>2-7 hari</strong> dan volume darah <strong>30-80 ml</strong> per siklus.</p>
      <ul>
        <li><strong>Normal:</strong> Kram ringan-sedang pada hari pertama, PMS ringan (mood swing, payudara sensitif), warna darah merah terang hingga merah tua</li>
        <li><strong>Perlu diperiksa:</strong> Kram yang tidak bisa ditahan walau sudah minum obat, haid lebih dari 7 hari, jarak antar haid kurang dari 21 hari atau lebih dari 35 hari, bercak di luar jadwal haid</li>
        <li><strong>Segera ke dokter:</strong> Perdarahan yang sangat banyak (ganti pembalut setiap jam selama beberapa jam), pingsan karena nyeri, atau tidak haid lebih dari 90 hari tanpa kehamilan</li>
      </ul>
