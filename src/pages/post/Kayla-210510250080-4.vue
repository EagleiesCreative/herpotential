    <h2>PCOS — Sindrom Ovarium Polikistik</h2>

    <div class="topic-card">
      <div class="topic-tag">PCOS</div>
      <h3>Kondisi yang Sangat Umum tapi Sering Tidak Terdiagnosis</h3>
      <p>PCOS mempengaruhi <strong>1 dari 10 perempuan</strong> di usia reproduktif. Di Indonesia, banyak yang tidak terdiagnosis hingga bertahun-tahun karena gejalanya dianggap "biasa."</p>
      <ul>
        <li><strong>Gejala umum:</strong> Haid tidak teratur atau tidak datang berbulan-bulan, pertumbuhan rambut di wajah/tubuh yang tidak biasa, jerawat yang resisten terhadap pengobatan biasa, kesulitan menurunkan berat badan</li>
        <li><strong>Diagnosis:</strong> Pemeriksaan USG ovarium + tes hormon (FSH, LH, AMH, androgen) — minta rujukan ke Sp.OG jika curiga PCOS</li>
        <li><strong>Pengelolaan:</strong> Perubahan gaya hidup (olahraga teratur + diet anti-inflamasi), metformin, atau pil KB untuk mengatur hormon — sesuai rekomendasi dokter</li>
        <li>PCOS <strong>dapat dikelola dengan baik</strong> — banyak perempuan dengan PCOS hamil secara alami</li>
      </ul>
    </div>

    <h2>Endometriosis</h2>

    <div class="topic-card">
      <div class="topic-tag">Endometriosis</div>
      <h3>Kondisi yang Butuh 7-10 Tahun untuk Terdiagnosis di Indonesia</h3>
      <p>Endometriosis terjadi ketika jaringan mirip lapisan rahim tumbuh di luar rahim. Kondisi ini mempengaruhi sekitar <strong>1 dari 10 perempuan</strong> dan merupakan salah satu penyebab nyeri haid ekstrem.</p>
      <ul>
        <li><strong>Tanda-tanda:</strong> Nyeri haid yang sangat berat (dismenore berat), nyeri saat berhubungan seksual, nyeri saat buang air besar atau kecil saat haid, kelelahan kronis</li>
        <li><strong>Diagnosis membutuhkan:</strong> USG khusus, MRI, atau laparoskopi (tindakan bedah minimal)</li>
        <li><strong>Pengelolaan:</strong> Hormonal (pil KB, injeksi progestin, IUD hormonal), pembedahan laparoskopi, atau kombinasi keduanya</li>
        <li>Endometriosis <strong>bukan hambatan untuk kehamilan</strong> — tapi perlu penanganan tepat dan sedini mungkin</li>
      </ul>
