  <h3>3. Distribusi Ulang Beban Perawatan</h3>
  <p>Kebijakan cuti ayah yang lebih panjang dan didorong penggunaannya adalah salah satu intervensi paling efektif yang terbukti dalam riset komparatif. Indonesia saat ini hanya memberikan cuti ayah 2 hari. Dua hari. Sementara Islandia memberikan 90 hari dan Swedia 240 hari.</p>

