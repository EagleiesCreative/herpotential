  <h2>Apa Itu Ekonomi Perawatan?</h2>
  <p>Care economy atau ekonomi perawatan mencakup semua kegiatan yang bertujuan untuk merawat, mendukung, dan mengembangkan manusia — baik yang dibayar maupun tidak. Ini termasuk: memasak dan membersihkan rumah, merawat bayi dan anak, mendampingi anak mengerjakan PR, merawat orang tua atau anggota keluarga yang sakit, serta menjaga kesehatan dan kesejahteraan emosional seluruh anggota keluarga.</p>
  <p>Sebagian pekerjaan ini dibayar — perawat, guru, pengasuh anak (babysitter), asisten rumah tangga. Tapi sebagian besar — dan yang paling berat — tidak dibayar sama sekali. Dan secara global, 75% dari kerja perawatan tak berbayar ini dilakukan oleh perempuan.</p>

