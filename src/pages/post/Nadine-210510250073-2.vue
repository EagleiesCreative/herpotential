  <h2>Apa yang Harus Berubah di Indonesia?</h2>
  <h3>1. Pengakuan dalam Sistem Statistik Nasional</h3>
  <p>BPS perlu mengembangkan "Satellite Account for Household Production" — sebuah sistem pengukuran yang menghitung nilai kerja domestik dan perawatan tak berbayar sebagai bagian dari neraca ekonomi nasional. Ini bukan sekadar soal angka; ini soal visibilitas. Yang tidak terukur tidak akan diperhatikan.</p>

  <h3>2. Infrastruktur Perawatan yang Dibiayai Negara</h3>
